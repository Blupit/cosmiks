// Login function
document.getElementById("loginForm").addEventListener("submit", function(event) {
  event.preventDefault();
  const username = document.getElementById("username").value;
  const password = document.getElementById("password").value;
  
  const user = users.find(u => u.username === username && u.password === password);
  if (user) {
    if (user.banned) {
      document.getElementById("errorMessage").innerText = "You are banned. if you think this is a mistake DM prq for an answer";
    } else {
      localStorage.setItem("loggedInUser", JSON.stringify(user));
      // Add rainbow animation class if the username matches
      if (username === "PrqYT" || username === "ChezyBred") {
        document.getElementById("username").classList.add("rainbow-text");
      }
      window.location.href = "index.html"; // Redirect to dashboard.html upon successful login
    }
  } else {
    document.getElementById("errorMessage").innerText = "Invalid username or password.";
  }
});
document.getElementById("editUsersForm").addEventListener("submit", function(event) {
  event.preventDefault();
  const userData = document.getElementById("userData").value;
  
  try {
    // Attempt to parse the provided JSON data
    const updatedUsers = JSON.parse(userData);
    
    // Update the users array with the new data
    users.length = 0; // Clear the existing array
    users.push(...updatedUsers); // Push the updated data into the array
    
    alert("Users updated successfully!");
  } catch (error) {
    alert("Error updating users. Please make sure the provided data is valid JSON.");
    console.error(error);
  }
});
